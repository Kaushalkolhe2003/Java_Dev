package Java_prac;

public class Variable {

	public static void main(String[] args) {
		int num=3;
		int num2=4;
		int result=num+num2;
		System.out.println(result);
		byte no=34;
		float x = 4.3f;
		double y=34.4666663;
		System.out.println(no+" "+x+" "+y);
		
		
		char C='k';
		System.out.println(C);
		boolean f=true;
		System.out.println(f);
	}
}


